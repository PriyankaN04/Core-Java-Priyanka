package com.java.corenikhil;

public class Opretors {

	public static void main(String[] args) {
		
		//Opretors-Addition
		
		int a = 25;
		int b = 64;
		int sum = a +b;
		System.out.println("Addition is = " +sum);
		
		
		//Opretors - Subtraction
		
		int s = 14;
		int d = 5;
		int subs = s - d;
		System.out.println("Subs is = " +subs);
		
		// Opretors - Multiplication 
		
		double f = 45;
		double g = 60;
		double mult = f * g;
		System.out.println("Multi is = " +mult);
		
		//Opretors - Divison 
		
		int j = 125;
		int k = 110;
		int div = j / k;
		System.out.println("Divison is = " +div);
		
		//Opretors - Modulas 
		
		int l = 74;
		int m = 15;
		int mod = l % m;
		System.out.println("Mod is = " +mod);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
}
