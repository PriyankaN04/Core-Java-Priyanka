package com.java.corenikhil;


public class Datatype {

	public static void main(String[] args) {
		
	//int
				
		int a = 20;
		int z = a;
		
		System.out.println(z);
		
	// short
		
		short age = 29;
		System.out.println(age);
		
		//long
		
		long phoneno = 54789687;
		
		System.out.println(phoneno);
		
		//byte
	
		byte mb = 125;
		System.out.println(mb);
		
		
		//char
		
		char S = 'B';
		System.out.println(S);
		
		//float 
		
		float average = 125.4f;
		System.out.println(average);
		
		//double 
		
		double percentage = 70.25;
		System.out.println(percentage);
		
		//boolean
		
		boolean nikhilismarried = true;
		System.out.println(nikhilismarried );
		
		
	}
}
