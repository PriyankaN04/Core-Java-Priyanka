package com.java.core.Loop;

public class ForLoop {

	public static void main(String[] args) {
	
		//Initialization
		//condition
		//Increment/Decrement
		
		System.out.println("Start :");
		
		//This is ascending order
		for (int i = 0; i < 5; i++) {
			System.out.println(i);		
		}
		
		System.out.println("END");
		
		//This is decending order
		for (int i = 5; i > 0; i--) {
			System.out.println(i);	
		}
		
		
		
		
	}
}
