package com.java.core.Loop;

public class Whileloop {
	
public static void main(String[] args) {
	

	//initialization
	//condition
	//code execution
	//Increment/Decrement

	
	//Increment
	System.out.println("Increment");
	int i = 0;
	while (i < 5) {
		System.out.println(i);
		i++;
	}
	
	//Decrement
	
	System.out.println("Decrement");
	int A = 5;
	while (A>0) {
		System.out.println(A);
		A--;
		
	}
	
}	
	
	
	
	
	
	
}
