package com.java.core.Loop;

public class whileloop {

	public static void main(String[] args) {
	
		
		//Increment
	int A = 0;
	while(A<10) {
		System.out.println(A);
		A++;
		
	}
	
	//Decrement
	int B = 10;
	while(B>0) {
		System.out.println(B);
		B--;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}
}
